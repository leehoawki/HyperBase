package com.toys.hyperbase.service;


public class Row {
    String key;
    
    String value;

    public Row() {

    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
